function [NN,Nr,Ns] = el3_ShapeFunctions(r,s)

%% Compute shape functions for 4 node quad

%%
NN = [(1/4).*((-1)+r).*((-1)+s),(-1/4).*(1+r).*((-1)+s),(1/4).*(1+r).*( ...
  1+s),(-1/4).*((-1)+r).*(1+s)];

Nr = [(1/4).*((-1)+s),(1/4).*(1+(-1).*s),(1/4).*(1+s),(1/4).*((-1)+(-1) ...
  .*s)];

Ns = [(1/4).*((-1)+r),(1/4).*((-1)+(-1).*r),(1/4).*(1+r),(1/4).*(1+(-1) ...
  .*r)];
